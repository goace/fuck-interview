#include <iostream>
using namespace std;

void swap(int *a, int *b)
{
    int t = *a;
    *a = *b;
    *b = t;
}

void qsort(int *a, int l, int r)
{
    if(l >= r)
        return;

    int x = a[l];
    int i = l+1, j = r;

    while(i <= j)
    {
        while(a[i] <= x) i++;
        while(a[j] > x)  j--;
        if(i <= j)
            swap(&a[i++], &a[j--]);
    }
    swap(&a[l], &a[j]); 
    qsort(a, l, j-1);
    qsort(a, j+1, r);
}

void sort(int *a, int n)
{
    qsort(a, 0, n-1);
}

